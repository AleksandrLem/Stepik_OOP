# –ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–µ –≤—Ä–µ–º—è
# –ö–ª–∞—Å—Å HourClock –¥–æ–ª–∂–µ–Ω –∏–º–µ—Ç—å –æ–¥–Ω–æ —Å–≤–æ–π—Å—Ç–≤–æ:

# hours ‚Äî —Å–≤–æ–π—Å—Ç–≤–æ, –¥–æ—Å—Ç—É–ø–Ω–æ–µ –¥–ª—è —á—Ç–µ–Ω–∏—è –∏ –∑–∞–ø–∏—Å–∏, –≤–æ–∑–≤—Ä–∞—â–∞—é—â–µ–µ
# —Ç–µ–∫—É—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —á–∞—Å–æ–≤. –ü—Ä–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–∏ —Å–≤–æ–π—Å—Ç–≤–æ –¥–æ–ª–∂–Ω–æ –ø—Ä–æ–≤–µ—Ä—è—Ç—å,
# —á—Ç–æ –Ω–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —è–≤–ª—è–µ—Ç—Å—è —Ü–µ–ª—ã–º —á–∏—Å–ª–æ–º, –ø—Ä–∏–Ω–∞–¥–ª–µ–∂–∞—â–∏–º –¥–∏–∞–ø–∞–∑–æ–Ω—É [1; 12],
# –≤ –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –≤–æ–∑–±—É–∂–¥–µ–Ω–æ –∏—Å–∫–ª—é—á–µ–Ω–∏–µ ValueError —Å —Ç–µ–∫—Å—Ç–æ–º:
# –ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–µ –≤—Ä–µ–º—è


class HourClock:
    def __init__(self, hours) -> None:
        self.hours = hours  # –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ–º —Å–≤–æ–π—Å—Ç–≤–æ –≤ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ç–æ—Ä–µ

    def get_hours(self):
        return self._hours  # –º–µ—Ç–æ–¥ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ self._hours

    def set_hours(self, hours):  # –º–µ—Ç–æ–¥ –ø—Ä–æ–≤–µ—Ä—è–µ—Ç –∏ –º–µ–Ω—è–µ—Ç –∑–∞–Ω—á–µ–Ω–∏–µ self._hours
        if isinstance(hours, int) and 1 <= hours <= 12:
            self._hours = hours
        else:
            raise ValueError('–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–µ –≤—Ä–µ–º—è')

    # —Å–≤–æ–π—Å—Ç–≤–æ hours, –≥–¥–µ –≥–µ—Ç—Ç–µ—Ä - get_hours, —Å–µ—Ç—Ç–µ—Ä - set_hours
    hours = property(get_hours, set_hours)


time = HourClock(7)

print(time.hours)
time.hours = 9
print(time.hours)


